class Recitation{
  String recitationId;
  String myBlessingId;
  String truthBlessingId;
  String email;
  String fileURL;


    get getRecitationId => this.recitationId;

  set setRecitationId(String recitationId) => this.recitationId = recitationId;

    get getMyBlessingId => this.myBlessingId;

  set setMyBlessingId( myBlessingId) => this.myBlessingId = myBlessingId;

    get getTruthBlessingId => this.truthBlessingId;

  set setTruthBlessingId( truthBlessingId) => this.truthBlessingId = truthBlessingId;

    get getEmail => this.email;

  set setEmail( email) => this.email = email;

    get getFileURL => this.fileURL;

  set setFileURL( fileURL) => this.fileURL = fileURL;


}